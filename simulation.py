def zone_risk(x, y):
    try:
        return float(_zone_risk(x, y)) if _zone_risk else 1.0
    except Exception:
        return 1.0

def movement_policy(state, x, y, day, counts, N):
    try:
        return _movement_policy(state, x, y, day, counts, N) if _movement_policy else None
    except Exception:
        return None

def transmission_modifier(sus_state, sx, sy, ix, iy, base_prob, day, counts, N):
    try:
        return float(_transmission_modifier(sus_state, sx, sy, ix, iy,
                                            base_prob, day, counts, N)
                     ) if _transmission_modifier else base_prob
    except Exception:
        return base_prob

def policy_label(day, counts, N):
    try:
        return str(_policy_label(day, counts, N)) if _policy_label else ''
    except Exception:
        return ''


