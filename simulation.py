# ── Agent class ───────────────────────────────────────────────────────────────
class Agent:
    def __init__(self, idx):
        self.x = np.random.uniform(5, MALL_W - 5)
        self.y = np.random.uniform(5, MALL_H - 5)
        angle  = np.random.uniform(0, 2 * np.pi)
        self.vx = SPEED * np.cos(angle)
        self.vy = SPEED * np.sin(angle)
        self.state = V if idx < int(VAX_RATE * N) else S
        self.timer = 0
        self.dest_x = np.random.uniform(10, MALL_W - 10)
        self.dest_y = np.random.uniform(10, MALL_H - 10)
        self.dest_timer = 0

    def move(self, day, counts):
        # Ask workshop if movement should be overridden
        self.dest_timer += 1
        override = movement_policy(self.state, self.x, self.y, day, counts, N)
        if override is not None:
            ox, oy = override
            self.dest_x = float(np.clip(ox, 0, MALL_W))
            self.dest_y = float(np.clip(oy, 0, MALL_H))
            self.dest_timer = 0
        elif self.dest_timer > np.random.randint(60, 180):
            self.dest_x = np.random.uniform(8, MALL_W - 8)
            self.dest_y = np.random.uniform(8, MALL_H - 8)
            self.dest_timer = 0

        dx = self.dest_x - self.x
        dy = self.dest_y - self.y
        dist = np.hypot(dx, dy) + 1e-6
        self.vx += (dx / dist) * 0.12 + np.random.normal(0, NOISE)
        self.vy += (dy / dist) * 0.12 + np.random.normal(0, NOISE)

        spd = np.hypot(self.vx, self.vy)
        if spd > SPEED:
            self.vx = self.vx / spd * SPEED
            self.vy = self.vy / spd * SPEED

        self.x = float(np.clip(self.x + self.vx, 0, MALL_W))
        self.y = float(np.clip(self.y + self.vy, 0, MALL_H))

        if self.x <= 0 or self.x >= MALL_W:
            self.vx *= -1
        if self.y <= 0 or self.y >= MALL_H:
            self.vy *= -1

    def tick(self):
        if self.state == E:
            self.timer += 1
            if self.timer >= INCUBATION:
                self.state = I
                self.timer = 0
        elif self.state == I:
            self.timer += 1
            if self.timer >= RECOVERY:
                self.state = R


